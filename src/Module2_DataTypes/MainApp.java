package Module2_DataTypes;

public class MainApp {
    public static void main(String[] args) {

        //TYPY DANYCH
        System.out.println(10); //liczby całkowite ->int
        System.out.println(10.5); //iczby zmiennoprzecinkowe -> double
        System.out.println("tekst"); //tekst podajemy w podwójnym cudzysłowie -> string
        System.out.println('a'); // pojedynczy znak podajemy w pojedynczym cudzyslowie -> char
        System.out.println(true); //prawda ->boolean
        System.out.println(false); //fałsz->boolean
    }
}
